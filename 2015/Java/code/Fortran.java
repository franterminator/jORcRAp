    private void ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonActionPerformed
        //get the users values
        n = Integer.parseInt(nComboBox.getSelectedItem().toString());
        try{
            double u = Double.parseDouble(UText.getText());
            double l = Double.parseDouble(LText.getText());
            double c = Double.parseDouble(CoefText.getText());
            
            //calls rio program
            Rio rio = new Rio();
            rio.start(u, l, c, n);
            Iterator<String> iterator = rio.getList().iterator();
            
            //set the labels
            PText.setText(iterator.next());
            AText.setText(iterator.next());
            BText.setText(iterator.next());
            CText.setText(iterator.next());
            vLText.setText(iterator.next());
            vDText.setText(iterator.next());
            vUText.setText(iterator.next());
            ZText.setText(iterator.next());
            YText.setText(iterator.next());
            XText.setText(iterator.next());
            
            //set the mathematica sol button
            MatButton.setEnabled(true);
        }
        catch(NumberFormatException ne){
            System.err.println("Bad input...");
            JOptionPane.showMessageDialog(null, "You have to write a rigth values as inputs.",
                    "ERROR",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ButtonActionPerformed

