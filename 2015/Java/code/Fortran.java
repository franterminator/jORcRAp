    private void MatButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MatButtonActionPerformed
        //1. try to get pel
        try {
            double pel = Double.parseDouble(PText.getText());
            StringBuilder text = new StringBuilder();
            //2. Create a vector for the solutions
            double sol[] = new double[n];
            //3. Get the values
            text.append("[");
            int i=0;
            for(; i<n; i++) {
                sol[i]=-(Math.pow(Math.E,pel*(i+1)/(n+1))-1)/(Math.pow(Math.E,pel)-1)+1;
                if(i==n-1)
                    text.append(String.format("%.4f]", sol[i]));
                else
                    text.append(String.format("%.4f,", sol[i]));
                
            }
            //4. print in a joption the solutions
            
            JOptionPane.showMessageDialog(null,"The mathematicas solution is:\n "+text,
                            "SOLUTION",JOptionPane.INFORMATION_MESSAGE);
        }catch(NumberFormatException ne){
            System.err.println("Bad input...");
            JOptionPane.showMessageDialog(null, "You have to write a rigth values as inputs.",
                    "ERROR",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_MatButtonActionPerformed

